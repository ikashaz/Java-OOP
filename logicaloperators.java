public class logicaloperators {
    public static void main(String[] args) {
        boolean a= true;
        boolean b= false;

        //logical &&
        boolean res = a && b;
        System.out.println(res);

        //logical &&
        boolean res1 = a || b;
        System.out.println(res1);

        // !a
        boolean res3=!a;
        System.out.println(res3);


    }
}
