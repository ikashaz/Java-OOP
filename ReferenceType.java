public class ReferenceType {
    public static void main(String[] args) {
        //String (Reference type)
        String myString="Hello Java!";
        System.out.println(myString);

        //ARRAY
        int[] myArray={1,2,3,4,5,6,7,8,9};
        System.out.print("My array:");
        for (int n:myArray){
            System.out.print(""+ n);
        }
    }

}
